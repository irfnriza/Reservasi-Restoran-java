    private boolean isUsernameExists(String username) {
        try (BufferedReader reader = new BufferedReader(new FileReader("Database/registrasi.txt"))) {
            String line;
            while ((line = reader.readLine()) != null) {
                // Split each line into username and password using ","
                String[] parts = line.split(",");
                String existingUsername = parts[0].trim(); // Assuming username is the first part
                // Check if the username already exists
                if (existingUsername.equals(username)) {
                    return true;
                }
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
        return false;
    }

