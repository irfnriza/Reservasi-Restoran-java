    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String newUsername = jTextField3.getText();
//        String newPassword = jTextField4.getText();
        // To retrieve the password as a char array
        char[] passwordChars = jPasswordField1.getPassword();

        // To convert the char array to a String (if needed)
        String newPassword = new String(passwordChars);
        
        if(jTextField3.getText().isEmpty() || newPassword.isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(this, "Mohon isi dengan lengkap");
        }else {
            // Check if the username already exists in the file
            if (isUsernameExists(newUsername)) {
                javax.swing.JOptionPane.showMessageDialog(this, "Gagal! Username sudah terdaftar!");
            } else {
                try (BufferedWriter writer = new BufferedWriter(new FileWriter("Database/registrasi.txt", true))) {
                    writer.write(newUsername + "," + newPassword);
                    writer.newLine();
                    javax.swing.JOptionPane.showMessageDialog(this, "Registrasi berhasil");
                    this.setVisible(false);
                    new welcome().setVisible(true);
                } catch (IOException e) {
                    e.printStackTrace();
                    javax.swing.JOptionPane.showMessageDialog(this, "Gagal menyimpan data registrasi.");
                }
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

