    private void jButton13ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton13ActionPerformed
        // TODO add your handling code here:
        // Mengambil teks dari jTextArea1
        String text = jTextArea1.getText();

        // Memeriksa apakah teks tidak kosong
        if (!text.isEmpty()) {
            // Memisahkan teks menjadi baris-baris
            String[] lines = text.split("\n");

            // Inisialisasi variabel total
            double totalHarga = 0;

            // Loop melalui setiap baris
            for (String line : lines) {
                // Memisahkan setiap baris menjadi item dan harga
                String[] parts = line.split("\t");

                // Memeriksa apakah format baris sesuai (item dan harga)
                if (parts.length == 2) {
                    try {
                        // Menguraikan nilai harga dan menambahkannya ke total
                        double harga = Double.parseDouble(parts[1].trim().replaceAll("[^\\d.]+", ""));
                        totalHarga += harga;
                    } catch (NumberFormatException e) {
                        // Teks harga tidak valid, tangani kesalahan di sini jika perlu
                        System.out.println("Format harga tidak valid pada baris: " + line);
                    }
                }
            }
            jTextField1.setText(String.valueOf("Rp. " +totalHarga + "00"));
            totalButtonPressed = true;
        }
    }//GEN-LAST:event_jButton13ActionPerformed

