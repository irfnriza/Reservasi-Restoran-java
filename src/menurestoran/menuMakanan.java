    public void deleteLine(){
        String namaFile = "Database/order.txt";

        try {
            // Baca seluruh konten file
            BufferedReader reader = new BufferedReader(new FileReader(namaFile));
            StringBuilder content = new StringBuilder();
            String line;

            // Baca dan simpan setiap baris ke StringBuilder
            while ((line = reader.readLine()) != null) {
                if (content.length() > 0) {
                    content.append(System.lineSeparator());
                }
                content.append(line);
            }

            // Tutup pembaca file
            reader.close();

            // Hapus baris terakhir
            int lastNewLineIndex = content.lastIndexOf(System.lineSeparator());
            if (lastNewLineIndex >= 0) {
                content.delete(lastNewLineIndex, content.length());
            }

            // Tulis kembali konten yang dimodifikasi ke dalam file
            BufferedWriter writer = new BufferedWriter(new FileWriter(namaFile));
            writer.write(content.toString());
            writer.newLine();

            // Tutup penulis file
            writer.close();

//            System.out.println("Baris terakhir berhasil dihapus dari file: " + namaFile);
        } catch (IOException e) {
            e.printStackTrace();
            System.out.println("Terjadi kesalahan saat menghapus baris terakhir: " + e.getMessage());
        }
    }
    
    private int nomorReservasi(){
        try {
            // Baca nomor reservasi terakhir dari file
            int lastReservationNumber = readLastReservationNumber();

            // Generate nomor reservasi baru
            int newReservationNumber = lastReservationNumber + 1;

            // Simpan nomor reservasi baru ke file
            writeReservationNumber(newReservationNumber);

            // Tampilkan nomor reservasi baru
            return newReservationNumber;
        } catch (IOException e) {
            e.printStackTrace();
            return 0;
        }
    }
    
    private static int readLastReservationNumber() throws IOException {
        File file = new File("Database/Reservasi_num.txt");

        // Jika file tidak ada, maka nomor reservasi terakhir dimulai dari 1
        if (!file.exists()) {
            return 0;
        }

        try (BufferedReader reader = new BufferedReader(new FileReader(file))) {
            String line = reader.readLine();
            if (line != null && !line.trim().isEmpty()) {
                return Integer.parseInt(line.trim());
            } else {
                // Jika string kosong, kembalikan nilai default
                return 0;
            }
        }
    }

    private static void writeReservationNumber(int number) throws IOException {
        try (BufferedWriter writer = new BufferedWriter(new FileWriter("Database/Reservasi_num.txt"))) {
            writer.write(String.valueOf(number));
            writer.newLine();
        }
    }
    
    private void saveOrderInfoToFile(String harga, int num) {
        try (BufferedWriter writer = new BufferedWriter(new FileWriter("Database/order.txt", true))) {
            writer.write(harga + "," + num);
            writer.newLine();
        } catch (IOException e) {
            e.printStackTrace();
        }
    }

