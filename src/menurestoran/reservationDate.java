    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String namaFile = "Database/order.txt";

        try {
            // Baca seluruh konten file
            BufferedReader reader = new BufferedReader(new FileReader(namaFile));
            StringBuilder content = new StringBuilder();
            String line;

            // Baca dan simpan setiap baris ke StringBuilder
            while ((line = reader.readLine()) != null) {
                if (content.length() > 0) {
                    content.append(System.lineSeparator());
                }
                content.append(line);
            }

            // Tutup pembaca file
            reader.close();

            // Memisahkan setiap data berdasarkan koma
            String[] entries = content.toString().split(System.lineSeparator());

            // Hapus indeks ke-1 (baris kedua) dari data terakhir
            if (entries.length > 0) {
                String lastEntry = entries[entries.length - 1];
                String[] lastData = lastEntry.split(",");

                if (lastData.length > 1) {
                    lastData[1] = "";
                    // Gabungkan kembali data terakhir yang telah dimodifikasi
                    StringBuilder modifiedLastData = new StringBuilder();
                    for (int i = 0; i < lastData.length; i++) {
                        if (i > 0) {
                            modifiedLastData.append(",");
                        }
                        modifiedLastData.append(lastData[i]);
                    }

                    // Ganti data terakhir dengan data yang telah dimodifikasi
                    entries[entries.length - 1] = modifiedLastData.toString();

                    // Tulis kembali konten yang dimodifikasi ke dalam file
                    BufferedWriter writer = new BufferedWriter(new FileWriter(namaFile));
                    for (String entry : entries) {
                        writer.newLine();
                        writer.write(entry);
                    }

                    // Tutup penulis file
                    writer.close();

//                    System.out.println("Indeks ke-1 pada baris terakhir berhasil dihapus dari file: " + namaFile);
                }
            }
        } catch (IOException e) {
            e.printStackTrace();
//            System.out.println("Terjadi kesalahan saat menghapus indeks ke-1 pada baris terakhir: " + e.getMessage());
        }
        this.setVisible(false);
        new MenuRestoran2().setVisible(true);
    }//GEN-LAST:event_jButton2ActionPerformed

