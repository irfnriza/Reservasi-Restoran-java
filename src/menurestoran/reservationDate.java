    private static boolean isValidDate(String dateStr, String format) {
        SimpleDateFormat sdf = new SimpleDateFormat(format);
        sdf.setLenient(false); // Menjadikan parsing ketat

        try {
            // Parsing tanggal, jika berhasil, maka format benar
            Date date = sdf.parse(dateStr);
            return true;
        } catch (ParseException e) {
            // Jika terjadi ParseException, maka format tidak sesuai
            return false;
        }
    }

    private static boolean isValidTime(String timeStr, String format) {
        SimpleDateFormat sdf = new SimpleDateFormat(format);
        sdf.setLenient(false); // Menjadikan parsing ketat

        try {
            // Parsing waktu, jika berhasil, maka format benar
            Date time = sdf.parse(timeStr);
            return true;
        } catch (ParseException e) {
            // Jika terjadi ParseException, maka format tidak sesuai
            return false;
        }
    }
    
    private void saveOrderInfoToFile(String tanggal, String waktu) {
        try (BufferedWriter writer = new BufferedWriter(new FileWriter("Database/order.txt", true))) {
            writer.write(tanggal + "," + waktu + ",");
        } catch (IOException e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, e);
        }
    }
    
    private boolean isDateExists(String meja, String date, String jam) {
         try (BufferedReader reader = new BufferedReader(new FileReader("Database/order.txt"))) {
            String line;
            while ((line = reader.readLine()) != null) {
                // Split each line into parts using ","
                String[] parts = line.split(",");
                if (parts.length >= 3) { // Pastikan ada cukup elemen dalam array
                    String existingMeja = parts[1].trim();
                    String existingDate = parts[2].trim(); 
                    String existingJam = parts[3].trim();

                    // Check if the date and jam already exist
                    if (existingMeja.equals(meja) && existingDate.equals(date) && existingJam.equals(jam)) {
                        return true;
                    }
                }
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
        return false;
    }
    
    private String getMeja(){
        String namaFile = "Database/order.txt";

        try {
            // Baca seluruh konten file
            BufferedReader reader = new BufferedReader(new FileReader(namaFile));
            String line;
            String lastEntry = null;

            // Baca file baris per baris dan simpan baris terakhir
            while ((line = reader.readLine()) != null) {
                lastEntry = line;
            }

            // Tutup pembaca file
            reader.close();

            // Jika ada setidaknya satu baris dalam file
            if (lastEntry != null) {
                // Split baris terakhir berdasarkan tanda koma
                String[] lastData = lastEntry.split(",");

                // Cetak indeks pertama (indeks 0) dari baris terakhir
                if (lastData.length > 0) {
//                    System.out.println("Indeks pertama dari baris terakhir: " + lastData[1]);
                    return lastData[1].trim();
                } else {
                    System.out.println("File tidak berisi data yang sesuai");
                }
            } else {
                System.out.println("File kosong");
            }
        } catch (IOException e) {
            e.printStackTrace();
            System.out.println("Terjadi kesalahan saat membaca file: " + e.getMessage());
        }
        return null;
    }

